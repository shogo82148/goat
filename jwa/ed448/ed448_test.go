package ed448

import (
	"crypto/subtle"
	"fmt"
	"testing"

	"github.com/shogo82148/goat"
	"github.com/shogo82148/goat/ed448"
)

type rawKey struct {
	priv ed448.PrivateKey
	pub  ed448.PublicKey
}

func (k *rawKey) PrivateKey() goat.PrivateKey {
	return k.priv
}

func (k *rawKey) PublicKey() goat.PublicKey {
	return k.pub
}

